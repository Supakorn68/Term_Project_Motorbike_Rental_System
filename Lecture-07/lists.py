# ฟังก์ชันสำหรับลบรายการที่ซ้ำกันออกจากลิสต์
def remove_duplicates(lst):
    # แปลงลิสต์ให้เป็นเซ็ต เพื่อลบรายการที่ซ้ำกันโดยอัตโนมัติ
    # จากนั้นแปลงกลับเป็นลิสต์เพื่อส่งคืนค่า
    return list(set(lst))

# ตัวอย่างการใช้งาน
numbers = [1, 2, 3, 1, 2, 4, 5, 6, 5, 4, 3]
print(remove_duplicates(numbers))
# ผลลัพธ์: [1, 2, 3, 4, 5, 6]